// pizzalari filterleme 
var a;
$(".divfilter ul li").click(function () {

    $(".divfilter ul li").css("border-bottom", "none")
    $(this).css("border-bottom", "3px solid rgb(173, 15, 20)")

    a = $(this).attr("filter")
    $("#pizza .qutu").each(function () {
        if ($(this).attr("mehsulnovu").includes(a) || a == "hamisi") {
            $(this).show()
        }
        else {
            $(this).hide()
        }
    });
})

// sebeti doldur
var sebetsay = 0;
var total = 0;
function sebetegondermenu(x, y) {
    $(".divsebetBody").append(`
    <div class="divmehsul">
        <div class="divmehsulsol">
              <img src="${arrmehsul[x][y].img}" alt="">
              <p>${arrmehsul[x][y].ad}</p> 
         </div>
         <div class="divmehsulsag">
              <span>${arrmehsul[x][y].qiymet}&#8380</span>
              <i class="fa-solid fa-xmark mehsulsil" ></i>
         </div>
    </div>
   `)
    sebetsay++
    $(".divsebetsay").html(sebetsay)
    $(".modalsebetHeader p span").html(sebetsay)
    total += Number(arrmehsul[x][y].qiymet)
    $(".divsebetFooter span").html(total)
}

// sebetdeki mehsulu silmek 
$(".divsebetBody").on("click", ".mehsulsil", function () {
    $(this).parent().parent().remove()

    sebetsay--
    $(".divsebetsay").html(sebetsay)
    $(".modalsebetHeader p span").html(sebetsay)

    total -= Number(parseInt($(this).parent().children().eq(0).html().replace(/[^0-9.]/g, "")))
    $(".divsebetFooter span").html(total)
    // console.log(parseInt($(this).parent().children().eq(0).html().replace(/[^0-9.]/g, "")));

})

